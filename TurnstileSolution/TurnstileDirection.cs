namespace TurnstileSolution;

/// <summary>
/// Represents the direction a person is moving through the turnstile.
/// </summary>
public enum TurnstileDirection
{
    /// <summary>
    /// Person is entering the building (value = 0).
    /// </summary>
    Enter = 0,

    /// <summary>
    /// Person is exiting the building (value = 1).
    /// </summary>
    Exit = 1
}
